package com.tao.sarastine;

public class Utt {
	
	private String utt, date;
	private boolean me;
	
	public void setUtt(String utt){
		this.utt = utt;
	}
	public String getUtt(){
		return utt;
	}
	public void setMe(boolean me){
		this.me = me;
	}
	public boolean getMe(){
		return me;
	}
	public void setDate(String date){
		this.date = date;
	}
	public String getDate(){
		return date;
	}
}
